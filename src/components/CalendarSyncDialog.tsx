import { useState } from "react";
import { useAuth } from "@/hooks/useAuth";
import { Button } from "@/components/ui/button";
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
  DialogTrigger,
} from "@/components/ui/dialog";
import { Label } from "@/components/ui/label";
import { Switch } from "@/components/ui/switch";
import { Input } from "@/components/ui/input";
import { Calendar, Download, Link2, Copy, Check, Smartphone } from "lucide-react";
import { toast } from "sonner";

interface CalendarSyncDialogProps {
  trigger?: React.ReactNode;
}

export default function CalendarSyncDialog({ trigger }: CalendarSyncDialogProps) {
  const { user } = useAuth();
  const [includeStrength, setIncludeStrength] = useState(true);
  const [includeCardio, setIncludeCardio] = useState(true);
  const [copied, setCopied] = useState(false);
  const [isDownloading, setIsDownloading] = useState(false);

  const baseUrl = `${import.meta.env.VITE_SUPABASE_URL}/functions/v1/calendar-export`;
  
  const getCalendarUrl = (format: "download" | "subscribe") => {
    const params = new URLSearchParams({
      user_id: user?.id || "",
      strength: includeStrength.toString(),
      cardio: includeCardio.toString(),
      format,
    });
    return `${baseUrl}?${params.toString()}`;
  };

  const subscriptionUrl = getCalendarUrl("subscribe");

  const handleDownload = async () => {
    if (!user) {
      toast.error("Du måste vara inloggad");
      return;
    }

    setIsDownloading(true);
    try {
      const response = await fetch(getCalendarUrl("download"));
      
      if (!response.ok) {
        const error = await response.json();
        throw new Error(error.error || "Kunde inte generera kalender");
      }

      const blob = await response.blob();
      const url = window.URL.createObjectURL(blob);
      const a = document.createElement("a");
      a.href = url;
      a.download = "gymdagboken-schema.ics";
      document.body.appendChild(a);
      a.click();
      window.URL.revokeObjectURL(url);
      document.body.removeChild(a);
      
      toast.success("Kalenderfil nedladdad!");
    } catch (error) {
      console.error("Download error:", error);
      toast.error(error instanceof Error ? error.message : "Kunde inte ladda ner kalender");
    } finally {
      setIsDownloading(false);
    }
  };

  const handleCopyUrl = async () => {
    try {
      await navigator.clipboard.writeText(subscriptionUrl);
      setCopied(true);
      toast.success("Länk kopierad!");
      setTimeout(() => setCopied(false), 2000);
    } catch {
      toast.error("Kunde inte kopiera länk");
    }
  };

  const handleAddToGoogleCalendar = () => {
    const googleUrl = `https://calendar.google.com/calendar/r?cid=${encodeURIComponent(subscriptionUrl)}`;
    window.open(googleUrl, "_blank");
  };

  return (
    <Dialog>
      <DialogTrigger asChild>
        {trigger || (
          <Button variant="outline" size="sm" className="gap-2">
            <Calendar className="h-4 w-4" />
            Synka kalender
          </Button>
        )}
      </DialogTrigger>
      <DialogContent className="sm:max-w-md">
        <DialogHeader>
          <DialogTitle className="flex items-center gap-2">
            <Calendar className="h-5 w-5 text-primary" />
            Synka till kalender
          </DialogTitle>
          <DialogDescription>
            Lägg till dina träningspass i din favoritkalender
          </DialogDescription>
        </DialogHeader>

        <div className="space-y-6 py-4">
          {/* Options */}
          <div className="space-y-4">
            <Label className="text-sm font-medium">Välj vad som ska synkas</Label>
            <div className="space-y-3">
              <div className="flex items-center justify-between">
                <Label htmlFor="strength" className="text-sm text-muted-foreground cursor-pointer">
                  Styrketräningsprogram
                </Label>
                <Switch
                  id="strength"
                  checked={includeStrength}
                  onCheckedChange={setIncludeStrength}
                />
              </div>
              <div className="flex items-center justify-between">
                <Label htmlFor="cardio" className="text-sm text-muted-foreground cursor-pointer">
                  Kardio-planer
                </Label>
                <Switch
                  id="cardio"
                  checked={includeCardio}
                  onCheckedChange={setIncludeCardio}
                />
              </div>
            </div>
          </div>

          {/* Download option */}
          <div className="space-y-3">
            <Label className="text-sm font-medium">Ladda ner fil</Label>
            <Button
              onClick={handleDownload}
              disabled={isDownloading || (!includeStrength && !includeCardio)}
              className="w-full gap-2"
              variant="outline"
            >
              <Download className="h-4 w-4" />
              {isDownloading ? "Laddar ner..." : "Ladda ner .ics-fil"}
            </Button>
            <p className="text-xs text-muted-foreground">
              Importera filen manuellt till din kalender-app
            </p>
          </div>

          {/* Subscription URL */}
          <div className="space-y-3">
            <Label className="text-sm font-medium">Prenumerations-URL</Label>
            <div className="flex gap-2">
              <Input
                value={subscriptionUrl}
                readOnly
                className="text-xs font-mono"
              />
              <Button
                variant="outline"
                size="icon"
                onClick={handleCopyUrl}
                className="shrink-0"
              >
                {copied ? <Check className="h-4 w-4 text-green-500" /> : <Copy className="h-4 w-4" />}
              </Button>
            </div>
            <p className="text-xs text-muted-foreground">
              Lägg till denna URL i din kalender-app för automatisk synk
            </p>
          </div>

          {/* Quick add buttons */}
          <div className="space-y-3">
            <Label className="text-sm font-medium">Snabbval</Label>
            <div className="grid grid-cols-1 gap-2">
              <Button
                variant="outline"
                onClick={handleAddToGoogleCalendar}
                disabled={!includeStrength && !includeCardio}
                className="justify-start gap-2"
              >
                <svg className="h-4 w-4" viewBox="0 0 24 24">
                  <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
                  <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
                  <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
                  <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
                </svg>
                Lägg till i Google Kalender
              </Button>
            </div>
          </div>

          {/* Instructions */}
          <div className="rounded-lg bg-muted/50 p-3 space-y-2">
            <div className="flex items-center gap-2 text-sm font-medium">
              <Smartphone className="h-4 w-4" />
              Instruktioner
            </div>
            <ul className="text-xs text-muted-foreground space-y-1 list-disc list-inside">
              <li><strong>iPhone:</strong> Kopiera URL → Inställningar → Kalender → Konton → Lägg till prenumererad kalender</li>
              <li><strong>Android:</strong> Kopiera URL → Google Kalender → Inställningar → Lägg till kalender → Från URL</li>
              <li><strong>Outlook:</strong> Kopiera URL → Lägg till kalender → Prenumerera från webben</li>
            </ul>
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
}
